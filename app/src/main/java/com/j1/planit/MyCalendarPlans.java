public class MyCalendarPlans {
    int no;
    String title;
    String date;
    String color;
    String share;
    String details;
    String alarm;


    public MyCalendarPlans(int no, String title, String date, String color, String share, String details, String alarm) {
        this.no = no;
        this.title = title;
        this.date = date;
        this.color = color;
        this.share = share;
        this.details = details;
        this.alarm = alarm;
    }
}
